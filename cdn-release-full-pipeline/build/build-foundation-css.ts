/**
 * build-foundation-css.ts — generate the brand-invariant static foundation CSS.
 *
 * Output: `packages/ui/foundation.css`
 *
 * Contents:
 *   1. Concatenated @oneui/tokens static CSS (layers, dimensions/scale + grid,
 *      typography, primitives, semantic, themes/light + dark, density/compact +
 *      open, materials).
 *   2. Static step-lookup slice (Neutral / Positive / Negative / Warning /
 *      Informative / Border step lookup). Brand-invariant by construction —
 *      generated from any brand's palette, the output bytes are identical.
 *
 * Consumers import `@oneui/ui/styles` (which prepends this file to the
 * component CSS Modules bundle at pack time — see pack-release.ts).
 */

import {
  writeFileSync, mkdirSync, existsSync, readFileSync,
} from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { ConvexHttpClient } from 'convex/browser';

import { api } from '../../convex/_generated/api';
import { buildAvailableScales } from '@oneui/shared/engine';
import {
  buildNewPaletteData,
  generateNewStepLookupCSSSplit,
} from '@oneui/ui/engine/computeNewStacking';

// ─── Env ──────────────────────────────────────────────────────────────────────
(function loadDotenv() {
  const file = resolve(dirname(fileURLToPath(import.meta.url)), '../../.env.local');
  if (!existsSync(file)) return;
  for (const line of readFileSync(file, 'utf8').split('\n')) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.+?)\s*$/i);
    if (!m) continue;
    if (process.env[m[1]] != null) continue;
    process.env[m[1]] = m[2].replace(/^['"]|['"]$/g, '');
  }
})();

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '../..');
const TOKENS_DIR = join(ROOT, 'packages/tokens/src/css');
const OUT_PATH = join(ROOT, 'packages/ui/foundation.css');

const CONVEX_URL =
  process.env.NEXT_PUBLIC_CONVEX_URL
  || process.env.STORYBOOK_CONVEX_URL
  || process.env.CONVEX_URL;

// Static CSS files in load order (mirrors apps/storybook/.storybook/preview.ts).
const STATIC_FILES = [
  'layers.css',
  'dimensions/scale.css',
  'dimensions/grid.css',
  'typography/typography.css',
  'primitives.css',
  'semantic.css',
  'themes/light.css',
  'themes/dark.css',
  'density/compact.css',
  'density/open.css',
  'materials.css',
];

async function fetchStaticStepLookupSlice(): Promise<string> {
  if (!CONVEX_URL) {
    throw new Error('CONVEX_URL not set. Need NEXT_PUBLIC_CONVEX_URL / STORYBOOK_CONVEX_URL / CONVEX_URL.');
  }
  console.log('→ Connecting to Convex for static-slice generation:', CONVEX_URL);
  const convex = new ConvexHttpClient(CONVEX_URL);
  const brands = await convex.query(api.brands.list, {});
  if (!brands.length) {
    throw new Error('No brands found in Convex; need at least one to generate the static step-lookup slice.');
  }

  // Pick any brand — output is brand-invariant by construction.
  // Prefer jio if present (most populated foundation data).
  const seed = brands.find((b: any) => b.name.toLowerCase() === 'jio') ?? brands[0];
  console.log(`→ Using brand "${seed.name}" as seed for the static step-lookup slice.`);

  const foundation = await convex.query(api.foundations.getBrandOverviewData, { brandId: seed._id });
  const availableScales = buildAvailableScales(
    foundation?.color?.config,
    foundation?.presetSelection,
  );
  const palette = buildNewPaletteData(availableScales, foundation?.appearanceConfig);
  if (!palette) {
    throw new Error('Failed to build palette data for static slice');
  }
  const { staticCSS } = generateNewStepLookupCSSSplit(palette);
  if (!staticCSS) {
    throw new Error('generateNewStepLookupCSSSplit returned empty staticCSS');
  }
  return `@layer brand {\n${staticCSS}\n}\n`;
}

async function main() {
  // 1. Concatenate static token CSS files.
  const parts: string[] = [];
  parts.push('/* OneUI foundation CSS — brand-invariant tokens.');
  parts.push(' * Generated by cdn-release-full-pipeline/build/build-foundation-css.ts.');
  parts.push(' * Concat order mirrors apps/storybook/.storybook/preview.ts. */');
  parts.push('');

  for (const rel of STATIC_FILES) {
    const file = join(TOKENS_DIR, rel);
    if (!existsSync(file)) {
      throw new Error(`Missing token CSS file: ${file}`);
    }
    parts.push(`/* ── @oneui/tokens/${rel} ──────────────────────────────── */`);
    parts.push(readFileSync(file, 'utf8').trimEnd());
    parts.push('');
  }

  // 2. Append static step-lookup slice from any brand (brand-invariant).
  parts.push('/* ── static step-lookup slice (Neutral/Positive/Negative/Warning/Informative/Border) ── */');
  parts.push(await fetchStaticStepLookupSlice());

  const css = parts.join('\n');

  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, css);

  const kb = (css.length / 1024).toFixed(1);
  console.log(`→ Wrote ${OUT_PATH} (${kb} KB)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
