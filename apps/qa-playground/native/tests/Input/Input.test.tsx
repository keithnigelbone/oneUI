describe('Input — functional: events', () => {
  it('[fn] onChange fires with typed value', () => {
    const handler = vi.fn();
    render(wrap(<Input onChange={handler} placeholder="Type here" />));
    fireEvent.changeText(screen.getByPlaceholderText('Type here', HIDDEN_OPT), 'hello');
    expect(handler).toHaveBeenCalledWith('hello');
  });

  it('[fn] onChange fires with exact value each keystroke', () => {
    const handler = vi.fn();
    render(wrap(<Input onChange={handler} placeholder="Type here" />));
    const el = screen.getByPlaceholderText('Type here', HIDDEN_OPT);
    fireEvent.changeText(el, 'a');
    fireEvent.changeText(el, 'ab');
    fireEvent.changeText(el, 'abc');
    expect(handler).toHaveBeenNthCalledWith(1, 'a');
    expect(handler).toHaveBeenNthCalledWith(2, 'ab');
    expect(handler).toHaveBeenNthCalledWith(3, 'abc');
  });

  it('[fn] onFocus fires when TextInput receives focus', () => {
    const handler = vi.fn();
    render(wrap(<Input onFocus={handler} placeholder="Focusable" />));
    fireEvent(screen.getByPlaceholderText('Focusable', HIDDEN_OPT), 'focus');
    expect(handler).toHaveBeenCalledTimes(1);
  });

  it('[fn] onBlur fires when TextInput loses focus', () => {
    const handler = vi.fn();
    render(wrap(<Input onBlur={handler} placeholder="Blurable" />));
    fireEvent(screen.getByPlaceholderText('Blurable', HIDDEN_OPT), 'blur');
    expect(handler).toHaveBeenCalledTimes(1);
  });

  it('[fn] onFocus and onBlur fire independently', () => {
    const focusH = vi.fn();
    const blurH = vi.fn();
    render(wrap(<Input onFocus={focusH} onBlur={blurH} placeholder="Both" />));
    const el = screen.getByPlaceholderText('Both', HIDDEN_OPT);
    fireEvent(el, 'focus');
    fireEvent(el, 'blur');
    expect(focusH).toHaveBeenCalledTimes(1);
    expect(blurH).toHaveBeenCalledTimes(1);
  });

  it('[fn] onSubmit fires with current value on submit', () => {
    const handler = vi.fn();
    render(wrap(<Input value="submit text" onSubmit={handler} placeholder="Submit" />));
    fireEvent(screen.getByPlaceholderText('Submit', HIDDEN_OPT), 'submitEditing', {
      nativeEvent: { text: 'submit text' },
    });
    expect(handler).toHaveBeenCalledWith('submit text');
  });

  it('[fn] disabled input: onFocus NOT fired (handleFocus guards isDisabled)', () => {
    const handler = vi.fn();
    render(wrap(<Input disabled onFocus={handler} placeholder="Dis focus" />));
    fireEvent(screen.getByPlaceholderText('Dis focus', HIDDEN_OPT), 'focus');
    expect(handler).not.toHaveBeenCalled();
  });

  it('[fn] disabled input → editable=false on TextInput', () => {
    render(wrap(<Input disabled placeholder="Disabled" />));
    expect(screen.getByPlaceholderText('Disabled', HIDDEN_OPT).props.editable).toBe(false);
  });

  it('[fn] readOnly input → readOnly=true on TextInput (editable stays true)', () => {
    // readOnly uses the TextInput readOnly prop, not editable=false.
    // editable=false is only used for disabled — readOnly keeps editable=true
    // so the field remains selectable/copyable by screen readers.
    render(wrap(<Input readOnly placeholder="ReadOnly" />));
    const el = screen.getByPlaceholderText('ReadOnly', HIDDEN_OPT);
    expect(el.props.readOnly).toBe(true);
    expect(el.props.editable).not.toBe(false);
  });
});

