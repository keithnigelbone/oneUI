/**
 * Input QA tests — smoke, functional, a11y, and bug-catching.
 *
 * Source: packages/ui-native/src/components/Input/Input.native.tsx
 *
 * ─── Figma API table ─────────────────────────────────────────────────────────
 *
 *   Property   Values                                          Figma type
 *   ─────────────────────────────────────────────────────────────────────
 *   size       XS | S | M | L                                  component property
 *   attention  medium | high                                    component property
 *   appearance auto|neutral|primary|secondary|sparkle|          variable mode
 *              negative|positive|warning|informative
 *   shape      default | pill                                   component property
 *   start      none|Icon|IconButton|Avatar|Image|ChipGroup|Text component property
 *   start2     none | Text                                      component property
 *   end        none | IconButton | Icon | Button | Text         component property
 *   end2       none | Text | Icon | IconButton                  component property
 *   state      idle|focus/active|filled|readOnly|feedback       component property
 *   disabled   true | false                                     variable mode
 *
 * ─── Native prop mapping ─────────────────────────────────────────────────────
 *
 *   Figma size → native InputSize
 *     XS    → 'xs' / 6  → minHeight: tokens.spacing['6']  (FIXED)
 *     S     → 's' / 8  → minHeight: tokens.spacing['8']
 *     M     → 'm' / 10 → minHeight: tokens.spacing['10']  (default)
 *     L     → 'l' / 12 → minHeight: tokens.spacing['12']
 *
 *   attention='medium' → outlined (transparent fill, hairline border at idle)
 *   attention='high'   → filled  (subtle background, no border at idle)
 *
 * ─── Component structure ─────────────────────────────────────────────────────
 *
 *   <Pressable testID={testID} accessible={false}
 *              importantForAccessibility="no-hide-descendants">   ← BUG-INPUT-3
 *     {start slot}
 *     <TextInput {...a11y} accessible={true} accessibilityLabel=... />
 *     {end slot}
 *   </Pressable>
 *
 *   IMPORTANT: The Pressable has importantForAccessibility='no-hide-descendants'.
 *   In RNTL, this flag causes standard queries (getByPlaceholderText, getByLabelText,
 *   getByDisplayValue) to treat the inner TextInput as hidden from accessibility.
 *   All TextInput queries use { includeHiddenElements: true } to bypass this filter.
 *   This is both a test workaround AND evidence of BUG-INPUT-3.
 *
 * ─── Bugs (raise to dev team) ────────────────────────────────────────────────
 *
 *   BUG-INPUT-1 · FIXED — XS size added to InputSize + SIZE_ALIASES + INPUT_SIZE_METRICS.
 *     InputSize is now 'xs'|'s'|'m'|'l' (f6/f8/f10/f12).
 *
 *   BUG-INPUT-2 · readOnly and disabled both set accessibilityState.disabled=true
 *     getInputAccessibilityProps returns:
 *       accessibilityState: { disabled: state.isDisabled || state.isReadOnly }
 *     Both disabled and readOnly surface as "disabled" to screen readers.
 *     VoiceOver/TalkBack cannot distinguish a read-only field (readable but
 *     immutable) from a disabled field (non-interactive entirely).
 *     File: packages/ui-native/src/components/Input/interface.ts:448-452
 *     Fix:  Use accessibilityState.disabled=false for readOnly; convey read-only
 *           semantics via accessibilityHint auto-generated by the component.
 *
 *   BUG-INPUT-3 · Container Pressable importantForAccessibility="no-hide-descendants"
 *     The outer Pressable sets importantForAccessibility='no-hide-descendants'
 *     unconditionally. On Android TalkBack, this hides ALL descendants including
 *     the inner TextInput from the accessibility tree. The same flag causes RNTL
 *     standard queries (getByPlaceholderText, getByLabelText, getByDisplayValue)
 *     to return "Unable to find" errors — workaround: { includeHiddenElements: true }.
 *     File: packages/ui-native/src/components/Input/Input.native.tsx:428
 *     Fix:  Change to importantForAccessibility='no' (hides container only, not children).
 */

