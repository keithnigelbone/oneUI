describe('Input — functional: size dimensions', () => {
  it('[fn] size="s" (8) → minHeight = tokens.spacing["8"]', () => {
    render(wrap(<Input size="s" testID="size-s" />));
    expect(flatStyle(screen.getByTestId('size-s', HIDDEN_OPT).props.style).minHeight).toBe(INPUT_SIZE_METRICS[8].minHeight);
  });

  it('[fn] size="m" (10) → minHeight = tokens.spacing["10"] (default)', () => {
    render(wrap(<Input size="m" testID="size-m" />));
    expect(flatStyle(screen.getByTestId('size-m', HIDDEN_OPT).props.style).minHeight).toBe(INPUT_SIZE_METRICS[10].minHeight);
  });

  it('[fn] size="l" (12) → minHeight = tokens.spacing["12"]', () => {
    render(wrap(<Input size="l" testID="size-l" />));
    expect(flatStyle(screen.getByTestId('size-l', HIDDEN_OPT).props.style).minHeight).toBe(INPUT_SIZE_METRICS[12].minHeight);
  });

  it('[fn] default size is "m" (minHeight = tokens.spacing["10"])', () => {
    render(wrap(<Input testID="size-default" />));
    expect(flatStyle(screen.getByTestId('size-default', HIDDEN_OPT).props.style).minHeight).toBe(INPUT_SIZE_METRICS[10].minHeight);
  });

  it('[fn] size scale: s < m < l (monotonically increasing)', () => {
    expect(INPUT_SIZE_METRICS[8].minHeight).toBeLessThan(INPUT_SIZE_METRICS[10].minHeight);
    expect(INPUT_SIZE_METRICS[10].minHeight).toBeLessThan(INPUT_SIZE_METRICS[12].minHeight);
  });

  it('[fn] string size aliases: s/m/l resolve to their respective sizes', () => {
    // Numeric size aliases (8/10/12) are no longer part of the public API —
    // resolveInputSize only recognises the t-shirt strings 'xs'|'s'|'m'|'l'.
    // Numeric inputs fall back to M (size=10) with a dev-mode warning.
    // This test verifies the string aliases resolve correctly.
    const { getByTestId: getS } = render(wrap(<Input size="s" testID="alias-ss" />));
    const ss = flatStyle(getS('alias-ss', HIDDEN_OPT).props.style);
    expect(ss.minHeight).toBe(INPUT_SIZE_METRICS[8].minHeight);

    const { getByTestId: getM } = render(wrap(<Input size="m" testID="alias-sm" />));
    const sm = flatStyle(getM('alias-sm', HIDDEN_OPT).props.style);
    expect(sm.minHeight).toBe(INPUT_SIZE_METRICS[10].minHeight);

    const { getByTestId: getL } = render(wrap(<Input size="l" testID="alias-sl" />));
    const sl = flatStyle(getL('alias-sl', HIDDEN_OPT).props.style);
    expect(sl.minHeight).toBe(INPUT_SIZE_METRICS[12].minHeight);

    render(wrap(<Input />));
  });
});

