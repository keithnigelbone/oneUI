const ATTENTIONS  = ['medium', 'high'] as const;
const SIZES       = ['xs', 's', 'm', 'l'] as const;

// ─── Helpers ──────────────────────────────────────────────────────────────────

function flatStyle(style: unknown): Record<string, unknown> {
  if (!style) return {};
  if (Array.isArray(style)) return Object.assign({}, ...(style as unknown[]).map(flatStyle));
  if (typeof style === 'object') return style as Record<string, unknown>;
  return {};
}

/**
 * The outer Pressable has importantForAccessibility='no-hide-descendants' (BUG-INPUT-3).
 * RNTL standard queries filter out the inner TextInput as "hidden".
 * Use { includeHiddenElements: true } to bypass this filter for TextInput queries.
 */
const HIDDEN_OPT = { includeHiddenElements: true } as const;

/**
 * White background wrapper for matrix/appearance tests.
 * Input uses attention="medium" (outlined) which gives a transparent fill —
 * against the dark HTML report panel the box is invisible. A white surface
 * makes the input border and the label above it both clearly visible.
 */
const wrapWhite = (el: React.ReactElement) =>
  wrap(<View style={{ backgroundColor: '#ffffff', padding: 8, width: 300 }}>{el}</View>);

// ─── Figma matrix: size × attention × state ───────────────────────────────────
//
// Rendered via InputField (label + Input) so the visual sketch shows something
// meaningful. The label text is outside the Pressable — visible in the sketch.
// The Input's Pressable (importantForAccessibility="no-hide-descendants") hides
// the TextInput content from the sketch, but the label and field shape are visible.
//
// States mirror the Figma matrix: idle / filled / readOnly / errorHighlight / disabled.

describe('Input — Figma matrix: size × attention × state', () => {
  const STATES = [
    { label: 'idle',           props: {} },
    { label: 'filled',         props: { defaultValue: 'Input text' } },
    { label: 'readOnly',       props: { readOnly: true, defaultValue: 'Input text' } },
    { label: 'errorHighlight', props: { invalid: true } },
    { label: 'disabled',       props: { disabled: true } },
  ] as const;

  for (const size of SIZES) {
    for (const attention of ATTENTIONS) {
      for (const { label, props } of STATES) {
        it(`[smoke] size="${size}" attention="${attention}" state="${label}" renders without crashing`, () => {
          expect(() =>
            render(wrapWhite(
              <InputField
                fullWidth
                label={`${size.toUpperCase()} · ${attention} · ${label}`}
                size={size}
                attention={attention}
                placeholder="Placeholder"
                {...props}
              />,
            )),
          ).not.toThrow();
        });
      }
    }
  }
});

