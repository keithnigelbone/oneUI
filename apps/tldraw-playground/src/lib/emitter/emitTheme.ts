// Emits the theme deliverable from the LIVE token state. theme.css + a
// Tailwind preset, both reflecting whatever the user has set in the Theme
// panel — not hardcoded defaults.

import { getTheme } from '@/lib/theme/themeStore'
import type { ThemeTokens } from '@/lib/theme/tokens'

const SETUP_README = `# tldraw-ui-builder — Theme setup

Two files: \`theme.css\` (CSS variables) and \`tailwind.preset.ts\` (Tailwind config extension).

## 1. Import the CSS

In your root stylesheet (e.g. Next.js \`app/globals.css\`, Vite \`src/index.css\`):

\`\`\`css
@import './theme.css';
@tailwind base;
@tailwind components;
@tailwind utilities;
\`\`\`

## 2. Add the Tailwind preset

In your \`tailwind.config.ts\`:

\`\`\`ts
import type { Config } from 'tailwindcss'
import uiPreset from './tailwind.preset'

const config: Config = {
  presets: [uiPreset],
  content: ['./app/**/*.{ts,tsx}', './components/**/*.{ts,tsx}'],
}
export default config
\`\`\`

Your generated components can now use \`bg-brand\`, \`text-fg\`, \`rounded-token\`, \`bg-success\`, etc.
`

function makeThemeCss(t: ThemeTokens): string {
  return `/* Generated by tldraw-ui-builder.
 * Drop into your global stylesheet (e.g. app/globals.css). */

:root {
  --ui-bg: ${t.bg};
  --ui-surface: ${t.surface};
  --ui-fg: ${t.fg};
  --ui-muted: ${t.muted};
  --ui-border: ${t.border};
  --ui-brand: ${t.brand};
  --ui-brand-fg: ${t.brandFg};
  --ui-success: ${t.success};
  --ui-success-fg: ${t.successFg};
  --ui-warning: ${t.warning};
  --ui-warning-fg: ${t.warningFg};
  --ui-danger: ${t.danger};
  --ui-danger-fg: ${t.dangerFg};
  --ui-info: ${t.info};
  --ui-info-fg: ${t.infoFg};
  --ui-radius: ${t.radius}px;
  --ui-radius-sm: ${t.radiusSm}px;
  --ui-radius-lg: ${t.radiusLg}px;
  --ui-font-sans: ${t.fontSans};
}
`
}

