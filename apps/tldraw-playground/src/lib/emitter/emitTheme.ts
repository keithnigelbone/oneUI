export interface EmitThemeFile {
  filename: string
  language: 'css' | 'typescript' | 'markdown'
  code: string
}

export function emitTheme(): EmitThemeFile[] {
  const tokens = getTheme()
  return [
    { filename: 'theme.css', language: 'css', code: makeThemeCss(tokens) },
    { filename: 'tailwind.preset.ts', language: 'typescript', code: TAILWIND_PRESET },
    { filename: 'README.md', language: 'markdown', code: SETUP_README },
  ]
}
